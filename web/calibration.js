  function poll() {
    fetch('/api/v1/setup/status', { credentials: 'same-origin' })
      .then(function (r) {
        if (r.status === 401 || r.status === 403) {
          window.location.replace('/setup');
          return null;
        }
        if (!r.ok) throw new Error('HTTP ' + r.status);
        return r.json();
      })
      .then(function (p) {
        if (!p) return;
        if (!p.needed && p.applied) {
          window.location.replace('/');
          return;
        }
        if (!p.running && !p.done && p.needed && !p.error) {
          fetch('/api/v1/setup/start', { method: 'POST', credentials: 'same-origin' });
        }
        // Merge in live RPM samples from /api/v1/status if present
        if (liveStatusCache && liveStatusCache.fans && p.fans) {
          for (var i = 0; i < p.fans.length; i++) {
            for (var j = 0; j < liveStatusCache.fans.length; j++) {
              if (liveStatusCache.fans[j].pwm_path === p.fans[i].pwm_path) {
                if (p.fans[i].current_rpm == null && liveStatusCache.fans[j].rpm != null) {
                  p.fans[i].current_rpm = liveStatusCache.fans[j].rpm;
                }
              }
            }
          }
        }
        applyProgress(p);
      })
      .catch(function () {
        if (!inDemoMode) {
          inDemoMode = true;
          startDemo();
        }
      });
  }
  function pollLive() {
    fetch('/api/v1/status', { credentials: 'same-origin' })
      .then(function (r) { return r.ok ? r.json() : null; })
      .then(function (s) { if (s) liveStatusCache = s; })
      .catch(function () {});
  }


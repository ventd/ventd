  // ── phase mapping (backend → UI) ────────────────────────────────────
  // Synthesise probing_polarity / finalizing client-side per the design
  // (backend Progress.Phase never emits these strings).
  function uiPhase(p) {
    if (!p) return 'detecting';
    if (p.applied) return 'done';
    if (p.done && !p.error) return 'done';
    var phase = p.phase || 'detecting';
    var fans = p.fans || [];
    // probing_polarity: any fan testing polarity AND backend not yet
    // calibrating
    if (phase !== 'calibrating' && phase !== 'finalizing' && phase !== 'done') {
      var anyTesting = false;
      for (var i = 0; i < fans.length; i++) {
        if (fans[i].polarity_phase === 'testing') { anyTesting = true; break; }
      }
      if (anyTesting) return 'probing_polarity';
    }
    // finalizing: backend says calibrating but every controllable fan
    // is done (or skipped/error) — i.e. nothing left to sweep
    if (phase === 'calibrating' && fans.length > 0) {
      var allDone = true, sweptOne = false;
      for (var j = 0; j < fans.length; j++) {
        var f = fans[j];
        if (f.cal_phase === 'done' || f.cal_phase === 'skipped' || f.cal_phase === 'error') {
          if (f.cal_phase === 'done') sweptOne = true;
          continue;
        }
        allDone = false;
        break;
      }
      if (allDone && sweptOne) return 'finalizing';
    }
    return phase;
  }

  // Pipeline + bridge update ------------------------------------------------
  function paintPipeline(currentPhase) {
    if (!els.pipeline) return;
    var idx = -1;
    for (var i = 0; i < PHASES.length; i++) if (PHASES[i].id === currentPhase) { idx = i; break; }
    var doneIdx = currentPhase === 'done' ? PHASES.length : idx;
    var nodes = els.pipeline.querySelectorAll('.v2-pipe-step');
    for (var k = 0; k < nodes.length; k++) {
      nodes[k].classList.remove('done', 'active', 'pending');
      if (k < doneIdx) nodes[k].classList.add('done');
      else if (k === idx) nodes[k].classList.add('active');
      else nodes[k].classList.add('pending');
    }
    var pct;
    if (currentPhase === 'done') pct = 100;
    else if (idx < 0) pct = 0;
    else pct = ((idx + 0.4) / PHASES.length) * 100;
    if (els.bridgeProgress) els.bridgeProgress.style.width = pct.toFixed(1) + '%';
  }

  function updateBridgeStats(p, currentPhase, sweepStats) {
    var fans = p.fans || [];
    var viable = 0, done = 0;
    for (var i = 0; i < fans.length; i++) {
      var f = fans[i];
      // a fan is "viable" if its detect_phase is found OR it's a non-hwmon
      // (NVML) channel that doesn't need a tach pairing.
      if (f.detect_phase === 'found' || f.detect_phase === 'n/a' || f.type === 'nvidia') viable++;
      if (f.cal_phase === 'done' || f.cal_phase === 'skipped') done++;
    }

    // Channels live: the count of fans with detect_phase advancing
    var live = 0;
    for (var l = 0; l < fans.length; l++) {
      if (fans[l].detect_phase && fans[l].detect_phase !== 'pending') live++;
    }

    // Samples / sec — derived heuristically from phase
    var sps = 0;
    if (currentPhase === 'scanning_fans')    sps = 800  + Math.round(Math.sin(Date.now() * 0.002) * 120);
    if (currentPhase === 'detecting_rpm')    sps = 2400 + Math.round(Math.sin(Date.now() * 0.0015) * 220);
    if (currentPhase === 'probing_polarity') sps = 1800 + Math.round(Math.sin(Date.now() * 0.0025) * 140);
    if (currentPhase === 'calibrating')      sps = 4200 + Math.round(Math.sin(Date.now() * 0.001) * 420);
    if (currentPhase === 'finalizing')       sps = 620  + Math.round(Math.sin(Date.now() * 0.003) * 60);

    if (els.bstatChannels)   els.bstatChannels.firstChild.nodeValue = live;
    if (els.bstatChannelsOf) els.bstatChannelsOf.textContent = '/' + Math.max(viable, 0);
    if (els.bstatSps)        els.bstatSps.textContent = sps.toLocaleString();
    if (els.bstatTotal && sweepStats) els.bstatTotal.textContent = sweepStats.totalSamples.toLocaleString();
    if (els.bstatFans)       els.bstatFans.firstChild.nodeValue = done;
    if (els.bstatFansOf)     els.bstatFansOf.textContent = '/' + fans.length;

    var phaseDef = PHASES.find(function (x) { return x.id === currentPhase; }) || { short: '—', blurb: '' };
    var phaseIdx = PHASES.findIndex(function (x) { return x.id === currentPhase; });
    var eyebrow = phaseIdx >= 0
      ? 'Phase ' + ('0' + (phaseIdx + 1)).slice(-2) + ' / 07 · ' + phaseDef.short
      : (currentPhase === 'done' ? 'Complete' : '—');
    if (els.phaseEyebrow) els.phaseEyebrow.textContent = eyebrow;
    if (els.phaseTitle)   els.phaseTitle.textContent = PHASE_TITLES[currentPhase] || (p.phase_msg || 'Calibrating');
    if (els.phaseSub)     els.phaseSub.textContent = phaseDef.blurb || (p.phase_msg || '');
  }

  // ── elapsed clock ───────────────────────────────────────────────────
  var startedAt = null;
  function tickClock(p) {
    if (!startedAt) startedAt = Date.now();
    var elapsed = Math.floor((Date.now() - startedAt) / 1000);
    if (els.elapsed) els.elapsed.textContent = fmtClock(elapsed);
    if (!els.remaining) return;
    var fans = p.fans || [];
    if (!fans.length) { els.remaining.innerHTML = '<span class="dim">—</span>'; return; }
    var done = 0;
    for (var i = 0; i < fans.length; i++) {
      if (fans[i].cal_phase === 'done' || fans[i].cal_phase === 'skipped') done++;
    }
    if (done === 0) { els.remaining.innerHTML = '<span class="dim">—</span>'; return; }
    var rate = elapsed / done;
    var remain = Math.max(0, Math.round(rate * (fans.length - done)));
    els.remaining.textContent = '~' + fmtClock(remain);
  }


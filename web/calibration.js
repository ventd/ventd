  function progressFraction(p) {
    var fans = p.fans || [];
    if (!fans.length) return 0;
    var done = 0, total = fans.length;
    for (var i = 0; i < fans.length; i++) {
      if (fans[i].cal_phase === 'done' || fans[i].cal_phase === 'skipped') done++;
      else if (fans[i].cal_phase === 'calibrating') done += (fans[i].cal_progress || 0) / 100;
    }
    return clamp(done / total, 0, 1);
  }


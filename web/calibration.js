  if (q.indexOf('demo=1') !== -1 || q.indexOf('demo=true') !== -1) {
    inDemoMode = true;
    paintPipeline('detecting');
    startDemo();
  } else {
    paintPipeline('detecting');
    poll();
    pollTimer = setInterval(poll, POLL_INTERVAL);
    liveTimer = setInterval(pollLive, LIVE_INTERVAL);
    openSetupEvents();
  }

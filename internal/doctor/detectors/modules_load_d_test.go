func TestRULE_DOCTOR_DETECTOR_ModulesLoad_RespectsContextCancel(t *testing.T) {
	det := NewModulesLoadDetector([]string{"nct6687"}, &stubModulesFS{})
	ctx, cancel := context.WithCancel(context.Background())
	cancel()

	_, err := det.Probe(ctx, doctor.Deps{Now: fixedNow})
	if err == nil {
		t.Errorf("Probe on cancelled ctx returned nil err")
	}
}

func TestContentMentionsModule_SubstringNotFalseMatch(t *testing.T) {
	// "nct6687d" must not match "nct6687" — token-bounded only.
	if contentMentionsModule([]byte("nct6687d\n"), "nct6687") {
		t.Errorf("substring match on different module name")
	}
	// Explicit positive: a comment line doesn't count.
	if contentMentionsModule([]byte("# nct6687 comment only\n"), "nct6687") {
		t.Errorf("comment line treated as content")
	}
	// Whitespace + comma fields both work.
	if !contentMentionsModule([]byte("nct6687\n"), "nct6687") {
		t.Errorf("simple match failed")
	}
	if !contentMentionsModule([]byte("nct6687, corsair-cpro\n"), "corsair-cpro") {
		t.Errorf("comma-separated list match failed")
	}
}

func TestModulesLoadConfPath_FormatStable(t *testing.T) {
	got := modulesLoadConfPath("nct6687")
	want := "/etc/modules-load.d/ventd-nct6687.conf"
	if got != want {
		t.Errorf("modulesLoadConfPath = %q, want %q", got, want)
	}
}
